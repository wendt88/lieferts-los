<template>
    <div>
        <h1>Bestellung</h1>
        <form>
            <div class="form-group">
                <label>Lieferant</label>
                <input
                    v-model="order.supplier"
                    type="text"
                    class="form-control"
                    placeholder="Liefernant inenspeibn"
                    :readonly="readonly"
                >
            </div>
            <div
                class="row"
            >
                <div class="col">
                    <div class="form-group">
                        <h4>Mo√ü</h4>
                    </div>
                </div>
                <div class="col">
                    <div class="form-group">
                        <h4>Menge</h4>
                    </div>
                </div>
                <div class="col">
                    <div class="form-group">
                        <h4>Zuig</h4>
                    </div>
                </div>
            </div>
            <div
                v-for="(product, index) of order.products"
                :key="product.id"
                class="row"
            >
                <div class="col">
                    <div class="form-group">
                        <select
                            id="exampleFormControlSelect1"
                            v-model="order.products[index].unit"
                            class="form-control"
                            :disabled="disabled"
                        >
                            <option value="kg">
                                Kilo
                            </option>
                            <option value="g">
                                Gramm
                            </option>
                            <option value="pieces">
                                Stuck
                            </option>
                            <option value="l">
                                Liter
                            </option>
                            <option value="mv">
                                Maul voll
                            </option>
                            <option value="tiggisch">
                                Wollten awian
                            </option>
                            <option value="flietscha">
                                Schun awian a flietscha
                            </option>
                        </select>
                    </div>
                </div>
                <div class="col">
                    <div class="form-group">
                        <input
                            v-model="order.products[index].amount"
                            type="number"
                            class="form-control"
                            placeholder="Menge - sollet a Zohl sein bittschian üòò"
                            :readonly="readonly"
                        >
                    </div>
                </div>
                <div class="col">
                    <div class="form-group">
                        <input
                            v-model="order.products[index].description"
                            type="text"
                            class="form-control"
                            placeholder="Is Produkt woses gern hett"
                            :readonly="readonly"
                            @keyup="addNewRowIfLast(index)"
                        >
                    </div>
                </div>
            </div>
            <div
                v-if="errorMessage"
                class="alert alert-danger"
                role="alert"
            >
                {{ errorMessage }}
            </div>
            <button
                v-if="editable"
                type="button"
                class="btn btn-primary"
                @click="saveOrder"
            >
                Ouschickn
            </button>
        </form>
    </div>
</template>

<script>

// TODO: photo upload per product
// TODO: barcode scanner per product

export default {
    data: function () {
        return {
            order: {
                products: [
                    {},
                ],
            },
            editable: false,
            errorMessage: '',
        }
    },
    computed: {
        readonly () {
            return this.editable ? false : 'readonly'
        },
        disabled () {
            return this.editable ? false : 'disabled'
        }
    },
    watch: {
        async $route (to) {
            this.getOrder(to)
        }
    },
    created () {
        this.getOrder(this.$route.params.orderID)
    },
    methods: {
        async getOrder (id) {
            if (id === 'new') {
                this.editable = true
                this.order = {
                    products: [
                        {},
                    ],
                }
            }
            else {
                this.editable = false
                this.order = await this.$db.order(id)
            }
        },
        addNewRowIfLast (index) {
            if (!this.editable) {
                return
            }
            if (this.order.products.length-1 === index) {
                this.order.products.push({})
            }
        },
        async saveOrder () {
            this.editable = false
            this.errorMessage = ''
            try {
                this.order = await this.$db.saveOrder(this.order)
                this.$router.push({ name: 'order detail', params: { orderID: this.order.id } })
            }
            catch (e) {
                this.errorMessage = e.message
                this.editable = true
            }
        }
    }
}
</script>

